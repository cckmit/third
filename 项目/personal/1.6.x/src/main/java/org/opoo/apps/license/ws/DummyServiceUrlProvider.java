package org.opoo.apps.license.ws;

public class DummyServiceUrlProvider implements ServiceUrlProvider {
	private String serviceURL;
	
	public DummyServiceUrlProvider() {
		super();
	}

	public DummyServiceUrlProvider(String serviceURL) {
		super();
		this.serviceURL = serviceURL;
	}


	public String getServiceURL() {
		return serviceURL;
	}

	public void setServiceURL(String serviceURL) {
		this.serviceURL = serviceURL;
	}
}
