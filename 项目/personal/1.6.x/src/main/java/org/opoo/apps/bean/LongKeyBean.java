package org.opoo.apps.bean;

import org.opoo.ndao.domain.LongKeyDomain;

/**
 * 
 * @author Alex Lin(alex@opoo.org)
 *
 */
public class LongKeyBean extends SerializableEntity<Long> implements LongKeyDomain {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1416535641461146165L;

	private Long id;

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	public Long getKey() {
		return getId();
	}

	public void setKey(Long id) {
		setId(id);
	}

}
