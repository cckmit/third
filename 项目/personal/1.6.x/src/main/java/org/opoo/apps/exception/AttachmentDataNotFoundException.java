package org.opoo.apps.exception;

public class AttachmentDataNotFoundException extends AttachmentException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3906096567374504005L;

	public AttachmentDataNotFoundException() {
	}

	public AttachmentDataNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public AttachmentDataNotFoundException(String message) {
		super(message);
	}

	public AttachmentDataNotFoundException(Throwable cause) {
		super(cause);
	}

}
