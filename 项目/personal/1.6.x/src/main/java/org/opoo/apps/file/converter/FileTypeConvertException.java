package org.opoo.apps.file.converter;

public class FileTypeConvertException extends RuntimeException {

	private static final long serialVersionUID = 6727910686025760443L;

	public FileTypeConvertException(String inputFormat, String outputFormat, Throwable cause) {
		super("文件类型转换失败：" + inputFormat + " -> " + outputFormat, cause);
	}
	
	public FileTypeConvertException() {
		super();
	}

	public FileTypeConvertException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileTypeConvertException(String message) {
		super(message);
	}

	public FileTypeConvertException(Throwable cause) {
		super(cause);
	}
}
