package org.opoo.apps.license;

public class AppsLicenseWarningException extends AppsLicenseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7669560742461482850L;

	public AppsLicenseWarningException() {
		super();
	}

	public AppsLicenseWarningException(String string, Throwable throwable) {
		super(string, throwable);
	}

	public AppsLicenseWarningException(String string) {
		super(string);
	}

	public AppsLicenseWarningException(Throwable throwable) {
		super(throwable);
	}

}
