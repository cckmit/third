package cn.redflagsoft.base.bean;

import java.util.Date;

import org.opoo.apps.bean.LongKeyBean;

import cn.redflagsoft.base.audit.DomainIdentifier;


public class AuditLog extends LongKeyBean {
	private static final long serialVersionUID = 1L;
    private long userId;
    private String username;
    private Date timestamp;
    private String node;
    private String details;
    private String description;
    private String operation;
	private DomainIdentifier domain;
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public Date getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(Date timestamp) {
		this.timestamp = timestamp;
	}
	public String getNode() {
		return node;
	}
	public void setNode(String node) {
		this.node = node;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	public DomainIdentifier getDomain() {
		return domain;
	}
	public void setDomain(DomainIdentifier domain) {
		this.domain = domain;
	}
	/**
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}
}
