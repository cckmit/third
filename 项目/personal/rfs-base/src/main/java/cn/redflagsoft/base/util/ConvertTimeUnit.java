package cn.redflagsoft.base.util;

public class ConvertTimeUnit {
	public static String toCharacter(byte timeUnit){
		switch(timeUnit){
		case 1: return "年";
		case 2: return "月";
		case 3: return "周";
		case 4: return "日";
		case 5: return "时";
		case 6: return "分";
		case 7: return "秒";
		case 8: return "毫秒";
		case 9: return "天";
		default:
			return "";
		}
	}
}
