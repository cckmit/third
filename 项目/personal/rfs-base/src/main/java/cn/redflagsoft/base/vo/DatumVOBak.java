package cn.redflagsoft.base.vo;

/**
 * @author Alex Lin
 *
 */
public class DatumVOBak {
	public static final int OPERATION_ADD = 0;
	public static final int OPERATION_DELETE = 1;
	public static final int OEPRATION_CONVERT = 2;
	
	
	private int operation = OPERATION_ADD;
	private Long[] datumIds;
	private Long[] attachmentIds;
	/**
	 * @return the operation
	 */
	public int getOperation() {
		return operation;
	}
	/**
	 * @param operation the operation to set
	 */
	public void setOperation(int operation) {
		this.operation = operation;
	}
	/**
	 * @return the datumIds
	 */
	public Long[] getDatumIds() {
		return datumIds;
	}
	/**
	 * @param datumIds the datumIds to set
	 */
	public void setDatumIds(Long[] datumIds) {
		this.datumIds = datumIds;
	}
	/**
	 * @return the attachmentIds
	 */
	public Long[] getAttachmentIds() {
		return attachmentIds;
	}
	/**
	 * @param attachmentIds the attachmentIds to set
	 */
	public void setAttachmentIds(Long[] attachmentIds) {
		this.attachmentIds = attachmentIds;
	}
	
	
}
