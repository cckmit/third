package cn.redflagsoft.base.menu;


/**
 * 子菜单。
 * 
 * 子菜单本身也是菜单，采用继承方式。
 * @author Alex Lin(alex@opoo.org)
 * @since 1.5
 */
public interface Submenu extends Menu {
	
	/**
	 * 上级菜单。
	 * 
	 * @return
	 */
	Menu getSuperior();
	
	/**
	 * 指定当前子菜单是否是从 superior 里 parent 的子菜单继承而来的。
	 * 
	 * @return
	 */
	boolean isInherited();
}
