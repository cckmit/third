package com.beitie.bean;

public class Music {
    private String author;
    private int time;
    private String name;

    public void setAuthor(String author) {
        this.author = author;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAuthor() {
        return author;
    }

    public int getTime() {
        return time;
    }

    public String getName() {
        return name;
    }
}
